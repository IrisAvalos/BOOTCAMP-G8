//OPERADORES MATEMÁTICOS

//SUMA
var a = 10+4;
console.log(a);

//RESTA
var b = 8-3;
console.log(b);

//MULTIPLICACIÓN
var c = 20.88*4;
console.log(c);

//DIVISIÓN
var d = 40/10;
console.log(d);

//RESIDUO
var residuo = 20%3;
console.log(residuo);

//POTENCIA
var potencia = 4**3;
console.log(potencia);


//OPERADORES DE COMPARACIÓN

//IGUALDAD
var prueba = 8;
var prueba2 = 8;
console.log(prueba===prueba2);

//DISTINTOS
var numero = 6;
var numero1 = 2;
console.log(numero!==numero1);

//MAYOR
var ayuda = 15;
var ayuda1 = 20;
console.log(ayuda>ayuda1);

//MAYOR O IGUAL
console.log(15>=15)

//MENOR
console.log(16<25);

//MENOR O IGUAL
console.log(19<=29); 


//OPERADORES LÓGICOS

// O

console.log(4>5 || 8>9); 

// Y

console.log(6<10 && 12<15); 

//NEGACIÓN

console.log(!false);


//COMPARACIÓN DE TEXTOS (STRING)

var mensaje = "hola como estas";
var mensaje1 = "hola como estas";
console.log(mensaje!==mensaje1);

